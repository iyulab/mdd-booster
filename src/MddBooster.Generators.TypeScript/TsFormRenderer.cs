using System.Text;
using System.Text.Json;
using M3L.Native;
using MddBooster.Core.Semantic;

namespace MddBooster.Generators.TypeScript;

/// <summary>
/// Renders per-entity React form base components ({Entity}Form_gen.tsx).
/// Generated forms use FormSection/FormRow from @iyulab/enterprise.
/// FK fields (@reference) become slot placeholders.
/// </summary>
public static class TsFormRenderer
{
    // Match the convention used by all other TS renderers in this project.
    private const string Header = "// <auto-generated> mdd-booster; DO NOT EDIT.</auto-generated>";

    /// <summary>Returns a dictionary of entityName → tsx content.</summary>
    /// <param name="enums">
    /// The model's enums. Both the set of enum type names and per-value metadata
    /// (which values are <c>@system</c>) are read from here, so there is one source
    /// of enum information rather than a name set that can drift from the nodes.
    /// </param>
    public static IReadOnlyDictionary<string, string> RenderAll(
        IReadOnlyList<ResolvedModel> models,
        IReadOnlyList<EnumNode> enums)
    {
        ArgumentNullException.ThrowIfNull(models);
        ArgumentNullException.ThrowIfNull(enums);

        var enumNames = new HashSet<string>(enums.Select(e => e.Name), StringComparer.Ordinal);
        var withSystemValues = EnumValueVisibility.TypeNamesWithSystemValues(enums);

        var result = new Dictionary<string, string>(StringComparer.Ordinal);
        foreach (var model in models)
        {
            var content = RenderOne(model, enumNames, withSystemValues);
            if (content != null)
                result[TypeScriptTypeMapper.PascalCase(model.Name)] = content;
        }
        return result;
    }

    /// <summary>Which UI control a field renders as.</summary>
    private enum FormControl
    {
        /// <summary>Rendered by the caller through a slot, not by this component.</summary>
        Slot,
        Checkbox,
        Select,
        /// <summary>Multi-line — <c>text</c>, which m3l defines as length-unbounded.</summary>
        Textarea,
        /// <summary>Single-line: date, numbers, and sized strings all land here.</summary>
        Input,
    }

    /// <summary>
    /// Classifies a field to its control. <b>The single source of that decision.</b>
    /// </summary>
    /// <remarks>
    /// Two places need this answer and they must never disagree: the import list at
    /// the top of the file, and the element rendered further down. When they were
    /// computed separately, the import list drifted — importing a control nothing
    /// used, which surfaces in the consumer's build as TS6133
    /// (<c>declared but never read</c>) rather than anywhere the generator can see.
    /// Adding a control type is now a change in exactly one predicate.
    /// </remarks>
    private static FormControl ControlFor(FieldNode field, IReadOnlySet<string> enumNames)
    {
        if (HasAttribute(field, "reference") || HasAttribute(field, "slot"))
            return FormControl.Slot;
        if (string.Equals(field.Type, "boolean", StringComparison.OrdinalIgnoreCase))
            return FormControl.Checkbox;
        if (field.Type != null && enumNames.Contains(field.Type))
            return FormControl.Select;
        if (string.Equals(field.Type, "text", StringComparison.OrdinalIgnoreCase))
            return FormControl.Textarea;
        return FormControl.Input;
    }

    /// <summary>
    /// Minimum visible rows for a generated textarea.
    /// </summary>
    /// <remarks>
    /// Not optional. <c>&lt;u-textarea&gt;</c> auto-sizes and starts at a single row, so
    /// omitting this leaves it visually identical to a single-line input — the very
    /// symptom this rendering exists to fix. The attribute is <c>minRows</c>;
    /// <c>rows</c> does not exist on the component.
    /// The model carries no row count, so this stays a generator constant.
    /// </remarks>
    private const int TextareaMinRows = 3;

    /// <summary>
    /// The label map a field's <c>USelect</c> actually reads from.
    /// </summary>
    /// <remarks>
    /// Two independent adjustments compose here, and both the import list and the
    /// rendered element must agree on the result — computing it twice is how they
    /// drift apart:
    /// <list type="bullet">
    /// <item><c>@display_labels(X)</c> swaps which enum supplies the text.</item>
    /// <item>An enum carrying <c>@system</c> values supplies a narrowed choice map.</item>
    /// </list>
    /// The exclusion follows the enum whose labels are displayed, which is the same
    /// enum whose members the map is keyed by.
    /// </remarks>
    private static string EffectiveLabelMap(FieldNode field, IReadOnlySet<string> withSystemValues)
    {
        var baseName = GetDisplayLabelsOverride(field) is { Length: > 0 } ov
            ? TypeScriptTypeMapper.PascalCase(ov)
            : TypeScriptTypeMapper.PascalCase(field.Type!);

        return withSystemValues.Contains(baseName)
            ? EnumValueVisibility.SelectableLabelsName(baseName)
            : baseName + "Labels";
    }

    private static string? RenderOne(
        ResolvedModel model,
        IReadOnlySet<string> enumNames,
        IReadOnlySet<string> withSystemValues)
    {
        var storedFields = model.Fields
            .Where(f => f.Kind == FieldKind.Stored)
            .Where(f => !HasAttribute(f, "pk"))
            .Where(f => !IsInheritedTimestamp(f.Name))
            .ToList();

        if (storedFields.Count == 0) return null;

        var entityName = TypeScriptTypeMapper.PascalCase(model.Name);

        // Group fields by @group attribute (preserve insertion order).
        // "기타" is the default fallback — note explicit @group("기타") merges into the same bucket.
        var sections = new Dictionary<string, List<FieldNode>>(StringComparer.Ordinal);
        foreach (var field in storedFields)
        {
            var group = GetAttributeString(field, "group") ?? "기타";
            if (!sections.ContainsKey(group))
                sections[group] = [];
            sections[group].Add(field);
        }

        // Collect slot fields: @reference FK fields AND @slot-annotated non-FK fields.
        // @slot allows non-FK fields (e.g. a field with a custom auto-generate button) to be
        // rendered as slot placeholders rather than as inline form controls.
        var slotFields = storedFields
            .Where(f => HasAttribute(f, "reference") || HasAttribute(f, "slot"))
            .Select(f => TypeScriptTypeMapper.PascalCase(f.Name))
            .ToList();
        // Keep fkFields as alias for backwards-compat with downstream code.
        var fkFields = slotFields;

        // Collect enum imports needed — only from renderable (non-slot, non-reference) fields,
        // since slot fields render their enum types via the caller's slot content, not here.
        var enumImports = storedFields
            .Where(f => f.Type != null && enumNames.Contains(f.Type))
            .Where(f => !HasAttribute(f, "reference") && !HasAttribute(f, "slot"))
            .Select(f => f.Type!)
            .Distinct()
            .Select(TypeScriptTypeMapper.PascalCase)
            .OrderBy(e => e)
            .ToList();

        // Non-slot renderable fields — these determine which UI component imports are needed.
        var renderableFields = storedFields
            .Where(f => !HasAttribute(f, "reference") && !HasAttribute(f, "slot"))
            .ToList();
        var allFieldsAreSlots = renderableFields.Count == 0;

        // Label maps actually referenced by USelect — per enum field, the @display_labels(X)
        // override if present, else the field's own {Type}Labels. This is DISTINCT from the
        // TYPE imports (enumImports), which always follow f.Type for the value cast.
        // @display_labels lets one enum carry context-specific labels (e.g. SettlementStatus
        // shown as "수금" for Order, "지급" for Purchase) without forking the stored enum.
        // Computing the effective per-field map (not enum-type ∪ overrides) avoids importing
        // an enum's own {Type}Labels when every field of that type is overridden (would be unused).
        var labelMapNames = renderableFields
            .Where(f => f.Type != null && enumNames.Contains(f.Type))
            .Select(f => EffectiveLabelMap(f, withSystemValues))
            .Distinct()
            .ToList();

        var sb = new StringBuilder();
        sb.AppendLine(Header);

        // ReactNode is only used for FK slot types — only import when needed.
        if (fkFields.Count > 0)
            sb.AppendLine("import type { ReactNode } from 'react'");

        sb.AppendLine("import { FormSection, FormRow } from '@iyulab/enterprise'");

        // Import exactly the controls the rendered fields actually use — derived from
        // the same classifier the renderer dispatches on, so the two cannot disagree.
        // An unused import is not harmless here: it fails the consumer's build with
        // TS6133, somewhere this generator never runs.
        var usedControls = renderableFields
            .Select(f => ControlFor(f, enumNames))
            .ToHashSet();
        var uiImports = new List<string>();
        if (usedControls.Contains(FormControl.Input)) uiImports.Add("UInput");
        if (usedControls.Contains(FormControl.Textarea)) uiImports.Add("UTextarea");
        if (usedControls.Contains(FormControl.Select)) uiImports.Add("USelect");
        if (usedControls.Contains(FormControl.Checkbox)) uiImports.Add("UCheckbox");
        if (uiImports.Count > 0)
            sb.Append("import { ").Append(string.Join(", ", uiImports)).AppendLine(" } from '../components/ui'");
        sb.Append("import type { ").Append(entityName).AppendLine(" } from '../types/entities_gen'");

        if (enumImports.Count > 0)
        {
            var typeImports = string.Join(", ", enumImports);
            sb.Append("import type { ").Append(typeImports).AppendLine(" } from '../types/enums_gen'");
            // Label maps = effective per-field maps (override / selectable / own),
            // never an unused one.
            var labelImports = string.Join(", ", labelMapNames
                .OrderBy(n => n, StringComparer.Ordinal));
            sb.Append("import { ").Append(labelImports).AppendLine(" } from '../types/enum_labels_gen'");
            sb.AppendLine("import { enumToOptions } from '../lib/select-options'");
        }

        sb.AppendLine();

        // Slots type (only when FK fields exist).
        if (fkFields.Count > 0)
        {
            sb.Append("export type ").Append(entityName).AppendLine("FormSlots = {");
            foreach (var fk in fkFields)
                sb.Append("  ").Append(fk).AppendLine("?: ReactNode");
            sb.AppendLine("}");
            sb.AppendLine();
        }

        // FormBase function.
        // When all fields are FK slots, form/onChange are not used in JSX.
        // Use destructuring rename { form: _form } to satisfy noUnusedParameters.
        var formDestructure = allFieldsAreSlots ? "form: _form" : "form";
        var onChangeDestructure = allFieldsAreSlots ? "onChange: _onChange" : "onChange";
        sb.Append("export function ").Append(entityName).AppendLine("FormBase({");
        sb.Append("  ").AppendLine(formDestructure + ",");
        sb.Append("  ").AppendLine(onChangeDestructure + ",");
        if (fkFields.Count > 0) sb.AppendLine("  slots,");
        sb.AppendLine("}: {");
        sb.Append("  form: Partial<").Append(entityName).AppendLine(">");
        sb.Append("  onChange: (updates: Partial<").Append(entityName).AppendLine(">) => void");
        if (fkFields.Count > 0) sb.Append("  slots?: ").Append(entityName).AppendLine("FormSlots");
        sb.AppendLine("}) {");
        sb.AppendLine("  return (");
        sb.AppendLine("    <>");

        foreach (var (sectionTitle, fields) in sections)
        {
            sb.Append("      <FormSection title=\"").Append(sectionTitle).AppendLine("\">");
            RenderSectionRows(sb, fields, enumNames, withSystemValues);
            sb.AppendLine("      </FormSection>");
        }

        sb.AppendLine("    </>");
        sb.AppendLine("  )");
        sb.AppendLine("}");

        // Form-state helpers — empty defaults + entity→form mapper.
        // Closes the drift class where page EMPTY/handleEdit hand-enumerate fields and
        // silently omit newly-added m3l columns. Generated from stored (writable) fields only,
        // so Lookup/Rollup/Computed never leak into PATCH bodies.
        var camel = CamelCase(entityName);
        sb.AppendLine();
        sb.Append("export const empty").Append(entityName).Append(": Partial<").Append(entityName).Append("> = { ");
        sb.Append(string.Join(", ", storedFields.Select(f =>
            $"{TypeScriptTypeMapper.PascalCase(f.Name)}: {EmptyValue(f, enumNames)}")));
        sb.AppendLine(" }");
        sb.AppendLine();
        sb.Append("export function ").Append(camel).Append("FromEntity(row: ").Append(entityName).Append("): Partial<").Append(entityName).AppendLine("> {");
        sb.Append("  return { ");
        sb.Append(string.Join(", ", storedFields.Select(f =>
            $"{TypeScriptTypeMapper.PascalCase(f.Name)}: {FromEntityValue(f, enumNames)}")));
        sb.AppendLine(" }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    private static string CamelCase(string pascal) =>
        string.IsNullOrEmpty(pascal) ? pascal : char.ToLowerInvariant(pascal[0]) + pascal[1..];

    /// <summary>
    /// M3L numeric primitives that should render as `&lt;UInput type="number"&gt;` in generated forms.
    /// Mirrors the full numeric subset of <see cref="M3lPrimitives.All"/> — "integer"/"decimal" alone
    /// missed "long"/"short"/"byte"/"float"/"double", silently falling back to string-typed UInput
    /// (mismatched against the `number | null` entity field, breaking tsc) for any field using them.
    /// </summary>
    private static readonly IReadOnlySet<string> NumericTypes = new HashSet<string>(StringComparer.OrdinalIgnoreCase)
    {
        "integer", "long", "short", "byte", "float", "double", "decimal",
    };

    private static bool IsNumberType(string? t) => t != null && NumericTypes.Contains(t);

    /// <summary>Literal for the empty/new form state of a field (matches RenderField's coercion).</summary>
    private static string EmptyValue(FieldNode field, IReadOnlySet<string> enumNames)
    {
        var t = field.Type;
        var def = MddBooster.Core.Ast.FieldAttributes.EffectiveDefault(field);
        var isEnum = t != null && enumNames.Contains(t);
        var isBool = string.Equals(t, "boolean", StringComparison.OrdinalIgnoreCase);
        if (!string.IsNullOrEmpty(def))
        {
            if (isEnum) return $"'{TypeScriptTypeMapper.PascalCase(def!)}'";
            if (isBool) return def!;            // "true" / "false"
            if (IsNumberType(t)) return def!;   // numeric literal
            return $"'{def}'";                  // string default
        }
        if (isBool) return "false";
        if (IsNumberType(t)) return field.Nullable ? "null" : "0";
        // enum not_null without a default has no valid empty literal ('' breaks the EnumType union);
        // emit undefined (valid in Partial<T>) so the user must pick. nullable → null.
        if (isEnum) return field.Nullable ? "null" : "undefined";
        if (string.Equals(t, "date", StringComparison.OrdinalIgnoreCase)) return field.Nullable ? "null" : "''";
        return "''";                             // string / text / identifier(FK) / json / phone
    }

    /// <summary>Expression mapping an entity row field into form state (write fields only).</summary>
    private static string FromEntityValue(FieldNode field, IReadOnlySet<string> enumNames)
    {
        var prop = TypeScriptTypeMapper.PascalCase(field.Name);
        var t = field.Type;
        var isEnum = t != null && enumNames.Contains(t);
        var isBool = string.Equals(t, "boolean", StringComparison.OrdinalIgnoreCase);
        if (isBool)
        {
            var def = MddBooster.Core.Ast.FieldAttributes.EffectiveDefault(field) is { Length: > 0 } v ? v : "false";
            return $"row.{prop} ?? {def}";
        }
        if (IsNumberType(t)) return $"row.{prop}";
        if (isEnum) return $"row.{prop}";
        if (string.Equals(t, "date", StringComparison.OrdinalIgnoreCase)) return $"row.{prop}";
        return field.Nullable ? $"row.{prop} ?? ''" : $"row.{prop}";
    }

    private static void RenderSectionRows(
        StringBuilder sb,
        List<FieldNode> fields,
        IReadOnlySet<string> enumNames,
        IReadOnlySet<string> withSystemValues)
    {
        var i = 0;
        while (i < fields.Count)
        {
            var field = fields[i];
            var isFullWidth = IsFullWidth(field);

            if (isFullWidth)
            {
                sb.AppendLine("        <FormRow full>");
                sb.Append("          ").AppendLine(RenderField(field, enumNames, withSystemValues));
                sb.AppendLine("        </FormRow>");
                i++;
            }
            else if (i + 1 < fields.Count && !IsFullWidth(fields[i + 1]))
            {
                sb.AppendLine("        <FormRow>");
                sb.Append("          ").AppendLine(RenderField(field, enumNames, withSystemValues));
                sb.Append("          ").AppendLine(RenderField(fields[i + 1], enumNames, withSystemValues));
                sb.AppendLine("        </FormRow>");
                i += 2;
            }
            else
            {
                // unpaired half-width field
                sb.AppendLine("        <FormRow full>");
                sb.Append("          ").AppendLine(RenderField(field, enumNames, withSystemValues));
                sb.AppendLine("        </FormRow>");
                i++;
            }
        }
    }

    private static bool IsFullWidth(FieldNode field)
    {
        if (HasAttribute(field, "reference")) return true;
        if (HasAttribute(field, "slot")) return true;
        if (string.Equals(field.Type, "text", StringComparison.OrdinalIgnoreCase)) return true;
        return false;
    }

    private static string RenderField(
        FieldNode field,
        IReadOnlySet<string> enumNames,
        IReadOnlySet<string> withSystemValues)
    {
        var prop = TypeScriptTypeMapper.PascalCase(field.Name);
        var label = field.Description ?? prop;
        var required = !field.Nullable;
        var requiredAttr = required ? " required" : "";
        // @help("...") → label 아래 도움말(description). 라벨은 짧게 유지하고
        // 예시·부연 설명은 description footer로 분리한다 (필드 라벨 비대화 방지).
        var helpText = GetAttributeString(field, "help");
        var descAttr = !string.IsNullOrEmpty(helpText) ? $" description=\"{helpText}\"" : "";

        // Dispatch on the same classifier the import list uses (see ControlFor).
        var control = ControlFor(field, enumNames);

        // FK or @slot → slot placeholder.
        // Uses `!== undefined` check (not ??) so callers can pass null to suppress the field entirely.
        if (control == FormControl.Slot)
            return $"{{slots?.{prop} !== undefined ? slots.{prop} : <span className=\"text-gray-400 text-xs\">{label} slot</span>}}";

        if (control == FormControl.Checkbox)
            return $"<UCheckbox label=\"{label}\"{descAttr} checked={{form.{prop} ?? false}} onChange={{v => onChange({{ {prop}: v }})}} />";

        // text → UTextarea. m3l's `text` means length-unbounded (the SQL target emits
        // NVARCHAR(MAX) for it), so a single-line control contradicts the model.
        // minRows is load-bearing — see TextareaMinRows.
        if (control == FormControl.Textarea)
            return $"<UTextarea label=\"{label}\"{requiredAttr}{descAttr} minRows={{{TextareaMinRows}}} value={{form.{prop} ?? ''}} onChange={{v => onChange({{ {prop}: v || null }})}} />";

        if (control == FormControl.Select)
        {
            // Select is only classified for a field whose Type names a known enum.
            var enumTypeName = TypeScriptTypeMapper.PascalCase(field.Type!);
            // Which map supplies the choices — see EffectiveLabelMap. The stored/cast
            // type stays enumTypeName regardless: narrowing the choices never narrows
            // what may be stored.
            var labelMap = EffectiveLabelMap(field, withSystemValues);
            // USelect.value is T extends string, it cannot accept null.
            // Use '' as the empty-state sentinel; onChange receives '' for no-selection.
            var placeholder = field.Nullable ? $" placeholder=\"{label} 선택\"" : "";
            var onChangeCast = field.Nullable
                ? $"v => onChange({{ {prop}: (v || null) as {enumTypeName} | null }})"
                : $"v => onChange({{ {prop}: v as {enumTypeName} }})";
            return $"<USelect label=\"{label}\"{requiredAttr}{descAttr}{placeholder} value={{form.{prop} ?? ''}} options={{enumToOptions({labelMap})}} onChange={{{onChangeCast}}} />";
        }

        // date → UInput type="date"
        // Required date fields use || undefined (Partial<T> expects T | undefined, not null).
        // Nullable date fields use || null (entity type is string | null, null is the correct empty value).
        if (string.Equals(field.Type, "date", StringComparison.OrdinalIgnoreCase))
        {
            var dateEmpty = field.Nullable ? "null" : "undefined";
            return $"<UInput label=\"{label}\"{requiredAttr}{descAttr} type=\"date\" value={{form.{prop} ?? ''}} onChange={{v => onChange({{ {prop}: v || {dateEmpty} }})}} />";
        }

        // number types — onChange uses undefined (not null) because Partial<T> marks fields as T | undefined.
        if (IsNumberType(field.Type))
            return $"<UInput label=\"{label}\"{requiredAttr}{descAttr} type=\"number\" value={{form.{prop} != null ? String(form.{prop}) : ''}} onChange={{v => onChange({{ {prop}: v ? Number(v) : undefined }})}} />";

        // string (default)
        return $"<UInput label=\"{label}\"{requiredAttr}{descAttr} value={{form.{prop} ?? ''}} onChange={{v => onChange({{ {prop}: v }})}} />";
    }

    private static string? GetAttributeString(FieldNode field, string attrName)
    {
        var attr = MddBooster.Core.Ast.FieldAttributes.Find(field, attrName);
        if (attr?.Args is { Count: > 0 }
            && attr.Args[0].ValueKind == JsonValueKind.String)
        {
            return attr.Args[0].GetString();
        }
        return null;
    }

    private static bool HasAttribute(FieldNode field, string name) =>
        MddBooster.Core.Ast.FieldAttributes.Has(field, name);

    // @display_labels(EnumName) — first arg is an identifier (like @reference(Target)).
    // Returns the raw override enum name, or null when the field has no such attribute.
    private static string? GetDisplayLabelsOverride(FieldNode field)
    {
        var attr = MddBooster.Core.Ast.FieldAttributes.Find(field, "display_labels");
        if (attr?.Args is { Count: > 0 } args)
        {
            var p = args[0];
            return p.ValueKind == JsonValueKind.String ? p.GetString() : p.GetRawText();
        }
        return null;
    }

    private static bool IsInheritedTimestamp(string fieldName) =>
        string.Equals(fieldName, "created_at", StringComparison.OrdinalIgnoreCase)
        || string.Equals(fieldName, "updated_at", StringComparison.OrdinalIgnoreCase);
}
